export type DashboardCopy = (typeof COPY)[DashboardLocale];

export function getDashboardCopy(locale: DashboardLocale): DashboardCopy {
  return COPY[locale];
}

export function formatRelativeDate(iso: string, locale: DashboardLocale): string {
  const copy = getDashboardCopy(locale).common;
  const diff = Date.now() - new Date(iso).getTime();
  const days = Math.floor(diff / 86_400_000);
  if (days === 0) return copy.today;
  if (days === 1) return copy.yesterday;
  if (days < 7) return copy.daysAgo(days);
  return new Date(iso).toLocaleDateString(locale === 'tr' ? 'tr-TR' : 'en-US');
}

export function homeGreeting(locale: DashboardLocale, firstName: string): string {
  const h = new Date().getHours();
  const home = getDashboardCopy(locale).home;
  if (h < 12) return home.greetingMorning(firstName);
  if (h < 18) return home.greetingAfternoon(firstName);
  return home.greetingEvening(firstName);
}
